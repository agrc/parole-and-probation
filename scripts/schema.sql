CREATE TABLE offenders
(
    id int not null IDENTITY(1,1),
    offender_id integer,
    gender nchar(1),
    region smallint,
    agency nvarchar(30),
    supervisor_id nchar(8),
    supervisor_name nvarchar(35),
    agent_id integer,
    agent_name nvarchar(40),
    offender nvarchar(60),
    date_of_birth datetime,
    race nvarchar(30),
    legal_status nvarchar(10),
    legal_status_code nchar(1),
    legal_status_description nvarchar(30),
    supervision_start_date datetime,
    offender_location nvarchar(40),
    address_start_date datetime,
    address nvarchar(50),
    unit nvarchar(30),
    city nvarchar(30),
    county nvarchar(30),
    state nchar(2),
    zip integer,
    address_type_code nchar(1),
    address_type nvarchar(20),
    x decimal(24,20),
    y decimal(24,20),
    score decimal(5,2),
    offender_phone nvarchar(12),
    standard_of_supervision nvarchar(5),
    special_supervision nvarchar(30),
    last_attempted_field_contact integer,
    last_office_contact integer,
    last_successful_field_contact integer,
    last_field_contact datetime,
    field_contact_result nvarchar(15),
    offense_code nchar(1),
    primary_offense nvarchar(30),
    crime_degree nchar(2),
    offense_description nvarchar(80),
    earned_compliance_credit datetime,
    active_warrant bit,
    employer nvarchar(100),
    employer_address nvarchar(60),
    employer_phone nvarchar(12),
    gang_id integer,
    gang_name nvarchar(30),
    gang_type_id integer,
    gang_type nvarchar(20),
    alerts nvarchar(500),
    cautions nvarchar(3500),
    in_compliance bit,
    shape geography
)
